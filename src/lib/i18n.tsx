type Key = keyof typeof dict.en;

const Ctx = createContext<{ lang: Lang; t: (k: Key, vars?: Record<string, string|number>) => string; setLang: (l: Lang) => void }>({
  lang: "en", t: (k) => k as string, setLang: () => {},
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  useEffect(() => {
    const saved = (typeof localStorage !== "undefined" && localStorage.getItem("lang")) as Lang | null;
    if (saved === "en" || saved === "hi") setLangState(saved);
  }, []);
  const setLang = (l: Lang) => { setLangState(l); if (typeof localStorage !== "undefined") localStorage.setItem("lang", l); };
  const t = (k: Key, vars?: Record<string, string|number>) => {
    let s: string = (dict[lang][k] as string) ?? (dict.en[k] as string) ?? k;
    if (vars) for (const [vk, vv] of Object.entries(vars)) s = s.replace(`{${vk}}`, String(vv));
    return s;
  };
  return <Ctx.Provider value={{ lang, t, setLang }}>{children}</Ctx.Provider>;
}

export const useI18n = () => useContext(Ctx);
